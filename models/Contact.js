const mongoose = require('mongoose');

const constactSchema = mongoose.Schema({
    user: {
        type: mongoose.Schema.Types.ObjectId,
        ref: 'users'
    },
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    phone: {
        type: String
    },
    type: {
        type: String,
        default: 'personal'
    },
    address: {
        type: String,
        required: true
    }
});

module.exports = mongoose.model('contact', constactSchema);